"""Mock manager: sets up CLI and MCP mocks for eval scenarios.

Coordinates two mocking mechanisms:

1. **CLI mocking** — generates a mock shell script for the agent's CLI binary
   placed on ``PATH`` ahead of the real binary.  The script pattern-matches
   incoming commands and returns canned stdout / stderr / exit_code.

2. **MCP mocking** — writes a responses JSON file consumed by
   ``mock_mcp_server.py``.  The caller (``agent_setup``) patches the agent
   config to launch the mock server instead of the real MCP server.

Usage::

    from eval_runner.execution.mocking import MockConfig, MockManager

    manager = MockManager(work_dir=Path("/tmp/eval-scenario"), cli_binary_name="my-cli")
    ctx = manager.setup(MockConfig(
        cli_mocks=[{"command": "my-cli list", "stdout": "...", "exit_code": 0}],
        mcp_mocks=[{"tool": "get_status", "response": {"ok": True}}],
    ))
    # ... run eval ...
    manager.teardown()
"""

from __future__ import annotations

import json
import logging
import os
import shutil
import stat
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)


@dataclass
class MockConfig:
    """Parsed mock configuration from a scenario JSON."""

    cli_mocks: list[dict[str, Any]] = field(default_factory=list)
    mcp_mocks: list[dict[str, Any]] = field(default_factory=list)

    @property
    def has_cli_mocks(self) -> bool:
        return bool(self.cli_mocks)

    @property
    def has_mcp_mocks(self) -> bool:
        return bool(self.mcp_mocks)

    @property
    def has_mocks(self) -> bool:
        return self.has_cli_mocks or self.has_mcp_mocks


@dataclass
class MockContext:
    """Active mock state for a running scenario.

    Attributes:
        mock_dir: Directory containing mock artifacts.
        original_path: The ``PATH`` value before CLI mocks were applied.
        mcp_responses_file: Path to the MCP mock responses JSON, or None.
    """

    mock_dir: Path
    original_path: str
    mcp_responses_file: Path | None = None


class MockManager:
    """Sets up and tears down mocks for a single eval scenario run.

    Args:
        work_dir: The per-scenario work directory (e.g. ``/tmp/eval-run/my-scenario``).
    """

    def __init__(self, work_dir: Path, cli_binary_name: str = "cli") -> None:
        self.work_dir = work_dir
        self.cli_binary_name = cli_binary_name
        self.mock_dir = work_dir / ".mocks"
        self._original_path: str | None = None

    def setup(self, config: MockConfig) -> MockContext:
        """Create mock artifacts and apply environment changes.

        Args:
            config: Mock definitions from the scenario JSON.

        Returns:
            A ``MockContext`` holding the mock state.
        """
        self.mock_dir.mkdir(parents=True, exist_ok=True)
        self._original_path = os.environ.get("PATH", "")

        mcp_responses_file: Path | None = None

        if config.has_cli_mocks:
            self._setup_cli_mocks(config.cli_mocks)
            # Prepend mock dir to PATH so the mock CLI binary is found first.
            os.environ["PATH"] = f"{self.mock_dir}:{self._original_path}"
            logger.info(
                "CLI mocks active (%d rules), PATH prepended with %s",
                len(config.cli_mocks),
                self.mock_dir,
            )

        if config.has_mcp_mocks:
            mcp_responses_file = self._setup_mcp_mocks(config.mcp_mocks)
            logger.info(
                "MCP mocks active (%d tools), responses at %s",
                len(config.mcp_mocks),
                mcp_responses_file,
            )

        return MockContext(
            mock_dir=self.mock_dir,
            original_path=self._original_path,
            mcp_responses_file=mcp_responses_file,
        )

    # ------------------------------------------------------------------
    # CLI mocking
    # ------------------------------------------------------------------

    def _setup_cli_mocks(self, cli_mocks: list[dict[str, Any]]) -> None:
        """Generate a mock CLI shell script and its responses file."""
        bin_name = self.cli_binary_name
        responses_file = self.mock_dir / "cli_responses.json"
        responses_file.write_text(json.dumps(cli_mocks))

        script_path = self.mock_dir / bin_name
        # The mock script delegates to a tiny Python snippet that reads the
        # responses JSON, matches the command by prefix, and returns the
        # canned output.  If nothing matches, it falls through to the real
        # binary (found via the *original* PATH).
        script_content = (
            "#!/bin/bash\n"
            f"# Mock {bin_name} CLI — generated by eval framework\n"
            f"python3 -c '\n"
            f"import json, sys, os\n"
            f'responses = json.loads(open("{responses_file}").read())\n'
            f'full = "{bin_name} " + " ".join(sys.argv[1:])\n'
            f'for r in sorted(responses, key=lambda x: len(x.get("command", "")), reverse=True):\n'
            f'    p = r.get("command", "")\n'
            f"    if full.startswith(p):\n"
            f'        out = r.get("stdout", "")\n'
            f'        err = r.get("stderr", "")\n'
            f"        if out: print(out)\n"
            f"        if err: print(err, file=sys.stderr)\n"
            f'        sys.exit(r.get("exit_code", 0))\n'
            f"# No match — try the real {bin_name}\n"
            f"real = None\n"
            f'for d in os.environ.get("PATH", "").split(":"):\n'
            f'    if d == "{self.mock_dir}": continue\n'
            f'    candidate = os.path.join(d, "{bin_name}")\n'
            f"    if os.path.isfile(candidate) and os.access(candidate, os.X_OK):\n"
            f"        real = candidate\n"
            f"        break\n"
            f"if real:\n"
            f'    os.execv(real, ["{bin_name}"] + sys.argv[1:])\n'
            f"else:\n"
            f'    print("Mock {bin_name}: no match and real {bin_name} not found", file=sys.stderr)\n'
            f"    sys.exit(127)\n"
            f'\' "$@"\n'
        )

        script_path.write_text(script_content)
        script_path.chmod(script_path.stat().st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)

    # ------------------------------------------------------------------
    # MCP mocking
    # ------------------------------------------------------------------

    def _setup_mcp_mocks(self, mcp_mocks: list[dict[str, Any]]) -> Path:
        """Write the MCP responses JSON consumed by ``mock_mcp_server.py``."""
        responses_file = self.mock_dir / "mcp_responses.json"
        responses_file.write_text(json.dumps(mcp_mocks))
        return responses_file

    @staticmethod
    def mock_mcp_server_path() -> Path:
        """Return the absolute path to ``mock_mcp_server.py``."""
        return Path(__file__).parent / "mock_mcp_server.py"

    # ------------------------------------------------------------------
    # Teardown
    # ------------------------------------------------------------------

    def teardown(self) -> None:
        """Restore ``PATH`` and remove mock artifacts."""
        if self._original_path is not None:
            os.environ["PATH"] = self._original_path
            self._original_path = None

        if self.mock_dir.exists():
            shutil.rmtree(self.mock_dir, ignore_errors=True)
